main() {
  // basic looping using for loop
  for (int counter = 0; counter < 5; counter++) {
    print("mari mabar");
  }

  print("");

  // while
  // condition check first, then execution
  int i = 0;
  while (i < 5) {
    print("mari makan");
    i++;
  }

  print("");

  // do-while
  // execution first, then check the condition
  int j = 0;
  do {
    print("mari belajar");
    j++;
  } while (j < 5);
}
